    <p>
        In Canada, where I live, we're seeing a rise in hate crimes all across the board sharply rising ever since the COVID-19 pandemic started. From 2019 to
        2022, the number of hate crimes reported to police annually nearly doubled from around 1,950 to nearly 3,600. Despite the population of LGBTQ+ Canadians
        reaching one million in 2021, hate crimes have been sharply on the rise with a 41% increase from just 2018 to 2019, over half of which were violent.
    </p>
    <p>
        A quarter century since TDoR started, we are not seeing transphobia slowing down and people are feeling increasingly empowered to be hateful. In 2021,
        Representative Ayanna Pressley in the US House, read a list of 46 names. 46 trans people who had been murdered that year to date. Despite that, we are
        seeing our legal rights continue to be under attack like never before.
    </p>
    <p>
        In 2023, the Human Rights Campaign in the USA declared a national state of emergency for LGBTQ+ Americans, a first in its
        <u>over 40 years of history</u>. In a single legislative session, a record <u>550 anti-LGBTQ+ bills</u> were introduced across the USA,
        <u>over 80 of which became laws</u>. It is important more than ever before to protect our freedoms.
    </p>
