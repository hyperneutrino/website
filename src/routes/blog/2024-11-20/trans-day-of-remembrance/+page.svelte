        28th in 1998; Chanelle Pickett, an African-American transgender woman, who was murdered in Watertown, Massachusetts on November 20th, 1995; and Monique
        Thomas, an African-American transgender woman, who was murdered in Dorchester, Massachusetts in September of 1998.
