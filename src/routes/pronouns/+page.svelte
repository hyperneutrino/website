<h1 class="text-3xl">pronouns</h1>
<p>
    my pronouns are <b>she/they</b> meaning I request to be addressed by she/her or they/them. my preference varies (the ordering is largely because I prefer the
    aesthetic appearance of she/they over they/she) but both are always safe to use
</p>
<p>
    note that using they/them as singular pronouns is grammatically correct and has been in use in the english language since the 14th century, only being
    criticized as "incorrect" starting in the 18th century as a combination of linguistic prescriptivism and transphobia
</p>
<p>to use my pronouns:</p>
<ul class="list-inside list-disc">
    <li>[<b>she</b> runs/<b>they</b> run] to the store</li>
    <li>I saw [<b>her</b>/<b>them</b>] at the store</li>
    <li>this is [<b>her</b>/<b>their</b>] phone</li>
    <li>this phone is [<b>hers</b>/<b>theirs</b>]</li>
    <li>this phone belongs to Iris [<b>herself</b>/<b>themselves</b>/<b>themself</b>]</li>
</ul>
